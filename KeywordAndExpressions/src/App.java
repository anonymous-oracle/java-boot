package KeywordAndExpressions.src;

public class App {
    public static void main(String[] args) {
        double kilometers = (100 * 1.609344); // all the operators and operands in the assignment operation form an expression
        int highScore = 50; // excluding 'int', this is also an expression
        if (highScore == 50) { // the condition is an expression
            System.out.println("This is an expression"); // the string within the quotes is also the expression
        }










        
    }
}
