package IfKeywordAndCodeBlocks.src;

public class App {
    public static void main(String[] args) {
        int value = 5000;
        if (value < 5000) {
            System.out.println("Value is less");
        } else if (value == 5000) {
            System.out.println("Value is correct");
        } else {
            System.out.println("Value is more");
        }
    }
}
